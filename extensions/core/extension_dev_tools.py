    def __init__(self, root_dir: Optional[Path] = None):
        """
        Initialize the development tools.

        Args:
            root_dir: Root directory of uDOS installation (defaults to auto-detect)
        """
        if root_dir is None:
            # Auto-detect root (assuming this file is in core/services/)
            self.root = Path(__file__).parent.parent.parent
        else:
            self.root = Path(root_dir)

        self.extensions_dir = self.root / "extensions"
        self.templates_dir = self.extensions_dir / "templates"
        self.dev_dir = self.extensions_dir / "development"

        # Ensure development directories exist
        self.dev_dir.mkdir(exist_ok=True)
        self.templates_dir.mkdir(exist_ok=True)

    def create_extension_template(self, name: str, ext_type: str = "web",
                                port: int = 8080, author: str = "Anonymous") -> bool:
        """
        Create a new extension from template.

        Args:
            name: Extension name
            ext_type: Extension type (web, cli, service)
            port: Default port for web extensions
            author: Extension author

        Returns:
            True if created successfully, False otherwise
        """
        try:
            # Validate name
            if not self._validate_extension_name(name):
                print(f"❌ Invalid extension name: {name}")
                print("   Names must be lowercase, alphanumeric, and may contain hyphens")
                return False

            # Create extension directory
            ext_dir = self.dev_dir / name
            if ext_dir.exists():
                print(f"❌ Extension '{name}' already exists at {ext_dir}")
                return False

            ext_dir.mkdir(parents=True)

            # Create appropriate template based on type
            if ext_type == "web":
                return self._create_web_extension_template(ext_dir, name, port, author)
            elif ext_type == "cli":
                return self._create_cli_extension_template(ext_dir, name, author)
            elif ext_type == "service":
                return self._create_service_extension_template(ext_dir, name, author)
            else:
                print(f"❌ Unknown extension type: {ext_type}")
                return False

        except Exception as e:
            print(f"❌ Error creating extension template: {e}")
            return False

    def _validate_extension_name(self, name: str) -> bool:
        """Validate extension name format."""
        import re
        pattern = r'^[a-z0-9][a-z0-9-]*[a-z0-9]$|^[a-z0-9]$'
        return bool(re.match(pattern, name)) and len(name) <= 50

