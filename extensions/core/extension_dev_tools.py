    def list_development_extensions(self) -> List[Dict[str, Any]]:
        """List extensions in development."""
        extensions = []

        try:
            if not self.dev_dir.exists():
                return extensions

            for ext_dir in self.dev_dir.iterdir():
                if ext_dir.is_dir():
                    manifest_path = ext_dir / "manifest.json"
                    if manifest_path.exists():
                        try:
                            with open(manifest_path, 'r') as f:
                                manifest = json.load(f)

                            manifest['path'] = str(ext_dir)
                            extensions.append(manifest)
                        except Exception:
                            # If manifest is invalid, still include the directory
                            extensions.append({
                                'name': ext_dir.name,
                                'version': 'unknown',
                                'type': 'unknown',
                                'path': str(ext_dir),
                                'status': 'invalid_manifest'
                            })

            return extensions

        except Exception:
            return extensions

