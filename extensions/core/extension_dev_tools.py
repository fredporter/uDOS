    parser.add_argument('--author', default='Anonymous', help='Extension author')
    parser.add_argument('--list', action='store_true', help='List development extensions')
    parser.add_argument('--docs', metavar='NAME', help='Generate API docs for extension')

    args = parser.parse_args()

    tools = ExtensionDevTools()

    if args.create:
        success = tools.create_extension_template(
            args.create, args.type, args.port, args.author
        )
        sys.exit(0 if success else 1)

    elif args.list:
        extensions = tools.list_development_extensions()
        if extensions:
            print("🔧 DEVELOPMENT EXTENSIONS")
